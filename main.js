var linkedin = document.querySelector(".LinkIn");
var linkedtext = document.querySelector(".Linkedintext");
var github = document.querySelector(".Github");
var githubtext = document.querySelector(".Githubtext");
var resume = document.querySelector(".Resume");
var resumetext = document.querySelector(".Resumetext");



linkedin.addEventListener("mouseover", function () {
    githubtext.style.borderTop = "2px solid #F33A6A";
    github.style.color = "#F33A6A";
    githubtext.style.color = "#F33A6A";

    resumetext.style.borderTop = "2px solid #F33A6A";
    resume.style.color = "#F33A6A";
    resumetext.style.color = "#F33A6A";

})
linkedin.addEventListener("mouseleave", function () {
    githubtext.style.color = "#0CBABA";
    github.style.color = "#0CBABA";
    githubtext.style.borderTop = "2px solid #0CBABA";

    resumetext.style.color = "#0CBABA";
    resume.style.color = "#0CBABA";
    resumetext.style.borderTop = "2px solid #0CBABA";

})

